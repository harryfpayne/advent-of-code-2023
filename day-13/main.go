package main

import (
	"strings"
)

type Pattern [][]bool

func (p Pattern) GetHorizontalSymmetries(allowSmudge bool) int {
	puzzleW := len(p[0])
	puzzleH := len(p)

	for counter := 0; counter < puzzleH-1; counter++ {
		diff := 0
		for col := 0; col < puzzleW; col++ {
			for offset := 0; ; offset++ {
				lowRow := counter - offset
				highRow := counter + offset + 1
				if lowRow < 0 || highRow >= puzzleH {
					break
				}
				if p[lowRow][col] != p[highRow][col] {
					diff++
				}
			}
		}
		if allowSmudge && diff == 1 {
			return counter + 1
		}
		if !allowSmudge && diff == 0 {
			return counter + 1
		}
	}
	return 0
}

func (p Pattern) GetVerticalSymmetries(allowSmudge bool) int {
	inverted := make(Pattern, len(p[0]))

	for j := 0; j < len(p[0]); j++ {
		var column []bool
		for i := 0; i < len(p); i++ {
			column = append(column, p[i][j])
		}
		inverted[j] = column
	}

	return inverted.GetHorizontalSymmetries(allowSmudge)
}

func Parse(str string) []Pattern {
	str = strings.TrimSpace(str)

	var patterns []Pattern
	for _, pattern := range strings.Split(str, "\n\n") {
		var p Pattern
		for _, line := range strings.Split(pattern, "\n") {
			var l []bool
			for _, point := range line {
				l = append(l, point == '#')
			}
			p = append(p, l)
		}
		patterns = append(patterns, p)
	}

	return patterns
}

func main() {
	patterns := Parse(PUZZLE)

	count := 0
	for _, pattern := range patterns {
		row := pattern.GetHorizontalSymmetries(true)
		col := pattern.GetVerticalSymmetries(true)
		count += col
		count += row * 100
	}
	println(count)
}

